// Thumbnail sizes
add_image_size( 'wd-thumb-600', 600, 150, true );
add_image_size( 'wd-thumb-300', 300, 100, true );

/*
to add more sizes, simply copy a line from above
and change the dimensions & name. As long as you
upload a "featured image" as large as the biggest
set width or height, all the other sizes will be
auto-cropped.

To call a different size, simply change the text
inside the thumbnail function.

For example, to call the 300 x 100 sized image,
we would use the function:
<?php the_post_thumbnail( 'bones-thumb-300' ); ?>
for the 600 x 150 image:
<?php the_post_thumbnail( 'bones-thumb-600' ); ?>

You can change the names and dimensions to whatever
you like. Enjoy!
*/

add_filter( 'image_size_names_choose', 'wd_custom_image_sizes' );

function wd_custom_image_sizes( $sizes ) {
    return array_merge( $sizes, array(
        'wd-thumb-600' => __('600px by 150px'),
        'wd-thumb-300' => __('300px by 100px'),
    ) );
}


